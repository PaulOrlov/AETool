    private void upDateScene() {
        Platform.runLater(new Runnable() {
            Timeline timeline = new Timeline();

            private void animateHide() {
                FadeTransition ft = new FadeTransition(Duration.millis(200), sGridPane);
                ft.setFromValue(1.0);
                ft.setToValue(0.0);
                ft.setCycleCount(1);
                ft.setAutoReverse(false);
                ft.playFromStart();
                ft.setOnFinished(new EventHandler<ActionEvent>() {
                    @Override
                    public void handle(ActionEvent event) {
                        doUpdateLogic();
                        animateShow();
                    }
                });
            }
            
            private void doUpdateLogic(){
                if (comment != null) {
                    comment.setText(exObj.getComment());
                }
                Participant pObj = exObj.getParticipant();
                participantChBox.setValue(pObj);
            }

            private void animateShow() {
                FadeTransition ft = new FadeTransition(Duration.millis(600), sGridPane);
                ft.setFromValue(0.0);
                ft.setToValue(1.0);
                ft.setCycleCount(1);
                ft.setAutoReverse(false);
                ft.playFromStart();
            }

            @Override
            public void run() {
                Project project = (Project) exN.getLookup().lookup(Project.class);
                if(project != null){
                    participantChBox.getItems().clear();
                    pList = (ParticipantList) project.getLookup().lookup(ParticipantList.class);
                    for (Participant current : pList.getAllParticipants()) {
                        participantChBox.getItems().add(current);
                    }    
                }
                
                animateHide();
            }
        });
    }
    
    private void serialize() {
        SerializeCookie sc = exN.getLookup().lookup(SerializeCookie.class);
        if (sc != null) {
            sc.serialize();
        }
    }
    
    class PCL implements PropertyChangeListener {

        @Override
        public void propertyChange(PropertyChangeEvent evt) {
            upDateScene();
        }
    }
