    @Override
    public void componentOpened() {
        projectOnLookup = Utilities.actionsGlobalContext().lookupResult(Node.class);
        projectOnLookup.addLookupListener(this);

        if (!projectOnLookup.allInstances().isEmpty()) {
            exN = projectOnLookup.allInstances().iterator().next();
            exObj = (Experiment) exN.getLookup().lookup(Experiment.class);
        }
        if (exObj == null) {
            return;
        }
        upDateScene();
        pcl = new PCL();
        exObj.addPropertyChangeListener(pcl);
    }

    @Override
    public void componentClosed() {
        projectOnLookup.removeLookupListener(this);
        if (exObj != null && pcl != null) {
            exObj.removePropertyChangeListener(pcl);
        }
    }
    
    @Override
    public void resultChanged(LookupEvent le) {
        if (!projectOnLookup.allInstances().isEmpty()) {
            exN = projectOnLookup.allInstances().iterator().next();
            exObj = (Experiment) exN.getLookup().lookup(Experiment.class);
            if (exObj != null) {
                upDateScene();
                pcl = new PCL();
                exObj.addPropertyChangeListener(pcl);
            }
        }
    }
    
    private void createScene() {
        Parent root = null;
        try {
            
            root = (Parent) FXMLLoader.load(getClass().getResource("Editor.fxml"));
//            root = (Parent) FXMLLoader.load(getClass().getResource("FXMLDocument.fxml"));
        } catch (IOException ex) {
            Exceptions.printStackTrace(ex);
        }

        if (root == null) {
            return;
        }
        
        participantChBox = (ChoiceBox) root.lookup("#participant");
        comment = (TextArea) root.lookup("#comment");
        sGridPane = (GridPane) root.lookup("#Grid");
        
        Button saveExpBtn = (Button) root.lookup("#saveExpBtn");
        saveExpBtn.addEventFilter(MouseEvent.MOUSE_CLICKED, new EventHandler<MouseEvent>() {
            @Override
            public void handle(MouseEvent t) {
                Map<String, Object> map = new HashMap<String, Object>();
                map.put("participant", (Participant) participantChBox.getValue());
                map.put("comment", comment.getText());
                exObj.upDate(map);
                serialize();
                upDateScene();
            }
        });

        Scene scene = new Scene(root);
        fxContainer.setScene(scene);

        if (exObj != null) {
            upDateScene();
        }
    }

